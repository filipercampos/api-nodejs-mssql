module.exports = {
    // Adição
    add: (num1, num2) => {
      return num1 + num2;
    },
  
    // Subtração
    sub: (num1, num2) => {
      return num1 - num2;
    },
  
    // Multiplicação
    mult: (num1, num2) => {
      return num1 * num2;
    },
  
    // Divisão
    div: (num1, num2) => {
      return num1 / num2;
    },
  };